package com.rcpcompany.uibindings.grid.internal;

import org.eclipse.core.runtime.Platform;
import org.eclipse.core.runtime.Plugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class Activator extends Plugin {

	// The plug-in ID
	public static final String ID = "com.rcpcompany.uibindings.grid";

	/**
	 * <code>true</code> if tracing all calls to the model.
	 */
	public boolean TRACE_SOURCE_MODEL = false;

	private static Activator plugin;

	/**
	 * The constructor
	 */
	public Activator() {
	}

	@Override
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;

		if (isDebugging()) {
			TRACE_SOURCE_MODEL = Boolean.parseBoolean(Platform.getDebugOption(ID + "/trace/model"));
		}
	}

	@Override
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 * 
	 * @return the shared instance
	 */
	public static Activator getDefault() {
		return plugin;
	}

}
